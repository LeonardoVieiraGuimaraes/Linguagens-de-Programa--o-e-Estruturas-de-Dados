// Aqui está um exemplo de código em C que inclui palavras reservadas, comentários usando barras duplas (//) e comentários usando /* */, e uma formatação adequada com indentação:

#include <stdio.h>

int main() {
    // Declaração de variáveis
    int numero1 = 10;
    int numero2 = 20;
    
    /*
    Comentário de múltiplas linhas
    Esta parte do código realiza uma operação matemática
    */

    // Realiza a soma dos números
    int resultado = numero1 + numero2;
    
    // Exibe o resultado
    printf("A soma de %d e %d é: %d\n", numero1, numero2, resultado);

    return 0;
}

// A palavra reservada int é usada para declarar variáveis do tipo inteiro.
// Usamos comentários de uma linha com // para explicar o que cada parte do código faz.
// Também usamos comentários de múltiplas linhas com /* */ para adicionar descrições mais longas.
// A indentação é usada para melhorar a legibilidade do código, ajudando a identificar os blocos de código.
// Este código é simples e realiza a adição de dois números inteiros e exibe o resultado na tela. Os comentários ajudam a documentar o código e explicar sua funcionalidade. A indentação ajuda a organizar visualmente o código, tornando-o mais fácil de ler e entender.