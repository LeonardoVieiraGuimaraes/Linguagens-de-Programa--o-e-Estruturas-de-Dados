public class ExemploMetodosSR {
    // Método sem retorno
    public static void imprimirSoma(int a, int b) {
        int soma = a + b;
        System.out.println("A soma é: " + soma);
    }

    public static void main(String[] args) {
        imprimirSoma(5, 3); // Chamando o método sem retorno
    }
}
